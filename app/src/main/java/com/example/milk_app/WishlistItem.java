package com.example.milk_app;

public class WishlistItem {
    public String name;
    public String details;
    public int price;
    public int quantity;

    public WishlistItem(String name, String details, int price, int quantity) {
        this.name = name;
        this.details = details;
        this.price = price;
        this.quantity = quantity;
    }
}
